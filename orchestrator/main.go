var (
	tracer        trace.Tracer
	logger        *slog.Logger
	validIDRegex   = regexp.MustCompile(`^[a-zA-Z0-9_-]{1,64}$`)
	validFaultTypeRegex = regexp.MustCompile(`^[a-zA-Z0-9_-]{1,64}$`)
)

func init() {
	logger = slog.New(slog.NewJSONHandler(os.Stdout, &slog.HandlerOptions{Level: slog.LevelInfo}))
	tracer = otel.Tracer("orchestrator-service")
}

// ─────────────────────────────────────────────
// Request / Response models
// ─────────────────────────────────────────────

type ProvisionRequest struct {
	UserID string `json:"user_id"`
	Level  int    `json:"level"`
}

type ProvisionResponse struct {
	ClusterID string `json:"cluster_id"`
	Status    string `json:"status"`
	Message   string `json:"message,omitempty"`
}

type InjectFaultRequest struct {
	ClusterID string `json:"cluster_id"`
	FaultType string `json:"fault_type"`
}

type InjectFaultResponse struct {
	ClusterID string `json:"cluster_id"`
	FaultType string `json:"fault_type"`
	Status    string `json:"status"`
}

type HealthResponse struct {
	Status    string `json:"status"`
	Timestamp string `json:"timestamp"`
	Version   string `json:"version"`
}

type ErrorResponse struct {
	Error   string `json:"error"`
	Details string `json:"details,omitempty"`
}

// ─────────────────────────────────────────────
// Validation helpers
// ─────────────────────────────────────────────

func validateProvisionRequest(req *ProvisionRequest) error {
	if req.UserID == "" {
		return errors.New("user_id is required")
	}
	if !validIDRegex.MatchString(req.UserID) {
		return errors.New("user_id contains invalid characters")
	}
	if req.Level < 1 || req.Level > 5 {
		return errors.New("level must be between 1 and 5")
	}
	return nil
}

func validateInjectFaultRequest(req *InjectFaultRequest) error {
	if req.ClusterID == "" {
		return errors.New("cluster_id is required")
	}
	// cluster_id is generated by this service as "vcluster-<user_id>", e.g. "vcluster-U12345"
	if !validIDRegex.MatchString(req.ClusterID) {
		return errors.New("cluster_id contains invalid characters")
	}
	if req.FaultType == "" {
		return errors.New("fault_type is required")
	}
	if !validFaultTypeRegex.MatchString(req.FaultType) {
		return errors.New("fault_type contains invalid characters")
	}
	return nil
}

// ─────────────────────────────────────────────
// Response helpers
// ─────────────────────────────────────────────

func writeJSON(w http.ResponseWriter, status int, payload any) {
	w.Header().Set("Content-Type", "application/json")
	w.WriteHeader(status)
	if err := json.NewEncoder(w).Encode(payload); err != nil {
		logger.Error("failed to encode response", "error", err)
	}
}

func writeError(w http.ResponseWriter, status int, msg string) {
	writeJSON(w, status, ErrorResponse{Error: msg})
}

// ─────────────────────────────────────────────
// Handlers
// ─────────────────────────────────────────────

// HealthHandler returns service health and readiness status.
func HealthHandler(w http.ResponseWriter, r *http.Request) {
	writeJSON(w, http.StatusOK, HealthResponse{
		Status:    "ok",
		Timestamp: time.Now().UTC().Format(time.RFC3339),
		Version:   os.Getenv("APP_VERSION"),
	})
}

// ProvisionClusterHandler deploys an ephemeral vcluster for the given user.
func ProvisionClusterHandler(w http.ResponseWriter, r *http.Request) {
	ctx, span := tracer.Start(r.Context(), "ProvisionCluster")
	defer span.End()

	if r.Method != http.MethodPost {
		writeError(w, http.StatusMethodNotAllowed, "method not allowed")
		return
	}

	var req ProvisionRequest
	if err := json.NewDecoder(r.Body).Decode(&req); err != nil {
		span.SetStatus(codes.Error, "invalid request body")
		writeError(w, http.StatusBadRequest, "invalid request body")
		return
	}

	if err := validateProvisionRequest(&req); err != nil {
		span.SetStatus(codes.Error, err.Error())
		writeError(w, http.StatusUnprocessableEntity, err.Error())
		return
	}

	span.SetAttributes(
		attribute.String("user.id", req.UserID),
		attribute.Int("simulation.level", req.Level),
	)

	clusterID, err := provisionVCluster(ctx, req.UserID, req.Level)
	if err != nil {
		logger.ErrorContext(ctx, "cluster provisioning failed", "user_id", req.UserID, "error", err)
		span.SetStatus(codes.Error, err.Error())
		writeError(w, http.StatusInternalServerError, "cluster provisioning failed")
		return
	}

	logger.InfoContext(ctx, "cluster provisioned", "user_id", req.UserID, "cluster_id", clusterID, "level", req.Level)
	writeJSON(w, http.StatusAccepted, ProvisionResponse{
		ClusterID: clusterID,
		Status:    "provisioning",
		Message:   fmt.Sprintf("Ephemeral cluster for user %s is being provisioned", req.UserID),
	})
}

// InjectFaultHandler injects a specific misconfiguration into an active cluster.
func InjectFaultHandler(w http.ResponseWriter, r *http.Request) {
	ctx, span := tracer.Start(r.Context(), "InjectFault")
	defer span.End()

	if r.Method != http.MethodPost {
		writeError(w, http.StatusMethodNotAllowed, "method not allowed")
		return
	}

	var req InjectFaultRequest
	if err := json.NewDecoder(r.Body).Decode(&req); err != nil {
		span.SetStatus(codes.Error, "invalid request body")
		writeError(w, http.StatusBadRequest, "invalid request body")
		return
	}

	if err := validateInjectFaultRequest(&req); err != nil {
		span.SetStatus(codes.Error, err.Error())
		writeError(w, http.StatusUnprocessableEntity, err.Error())
		return
	}

	span.SetAttributes(
		attribute.String("cluster.id", req.ClusterID),
		attribute.String("fault.type", req.FaultType),
	)

	if err := injectFault(ctx, req.ClusterID, req.FaultType); err != nil {
		logger.ErrorContext(ctx, "fault injection failed", "cluster_id", req.ClusterID, "fault_type", req.FaultType, "error", err)
		span.SetStatus(codes.Error, err.Error())
		writeError(w, http.StatusInternalServerError, "fault injection failed")
		return
	}

	logger.InfoContext(ctx, "fault injected", "cluster_id", req.ClusterID, "fault_type", req.FaultType)
	writeJSON(w, http.StatusOK, InjectFaultResponse{
		ClusterID: req.ClusterID,
		FaultType: req.FaultType,
		Status:    "injected",
	})
}

